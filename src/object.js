function id_2_color(id)
{
    return [
        ((id >>  0) & 0xFF) / 0xFF,
        ((id >>  8) & 0xFF) / 0xFF,
        ((id >> 16) & 0xFF) / 0xFF,
    ]
}
function color_2_id(color)
{
    return (
        color[0] +
        (color[1] <<  8 )+
        (color[2] << 16)
    )
}



